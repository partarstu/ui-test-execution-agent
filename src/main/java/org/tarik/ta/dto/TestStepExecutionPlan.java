package org.tarik.ta.dto;

import org.tarik.ta.annotations.JsonClassDescription;
import org.tarik.ta.annotations.JsonFieldDescription;

import java.util.List;

@JsonClassDescription("the generated by you execution plan for a single action")
public record TestStepExecutionPlan(
        @JsonFieldDescription("original action unique ID") String actionUniqueId,
        @JsonFieldDescription("name of the identified by you tool for this action. If no tool is suitable for this action, this should" +
                " be empty") String toolName,
        @JsonFieldDescription("arguments for the identified by you tool for this action. If no tool is suitable for this action, " +
                "this should be an empty list") List<String> arguments
) {
}
